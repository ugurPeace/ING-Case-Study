// localStorage'tan çalışanları getir veya varsayılan listeyi yükle
function loadEmployeesFromStorage() {
  const storedEmployees = JSON.parse(localStorage.getItem("employees"));
  if (!storedEmployees) {
    // Eğer localStorage boşsa varsayılan listeyi ekle
    saveEmployeesToStorage(defaultEmployees);
    return defaultEmployees;
  }
  return storedEmployees;
}

// LocalStorage'daki listeyi güncelle
function saveEmployeesToStorage(employees) {
  localStorage.setItem("employees", JSON.stringify(employees));
}

// Çalışanları getir
export function getEmployees() {
  return loadEmployeesFromStorage();
}

